package operators;

public class relatinal {

public static void main(String[] args){
    System.out.println("===== Relational Operators =====");
		
    final int i14 = 10;
    final int i15 = 20;

    System.out.println(true==true);
    System.out.println(i14 == i15);	// false
    System.out.println(i14 != i15);	// true
    System.out.println(i14 > i15);	// false
    System.out.println(i14 < i15);	// true
    System.out.println(i14 >= i15);	// false
    System.out.println(i14 <= i15);	// true
    
    System.out.println();
    
}
}