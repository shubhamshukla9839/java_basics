package operators;

public class ternary {


    	public static void main(final String[] args){
		System.out.println("===== Ternary Operator =====");
		
		System.out.println(2 > 1 ? "2 is greater than one" : "2 is not less than one");
		System.out.println(2 < 1 ? "2 is greater than one" : "2 is not less than one");
    
}
}